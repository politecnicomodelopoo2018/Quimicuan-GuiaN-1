class Pedido(object):

    Fecha = None
    Plato = None
    Persona = None
    Hora = None
    Bool = None

    def SetFecha(self,Fecha):
        self.Fecha = Fecha

    def SetPlato(self,Plato):
        self.Plato = Plato

    def SetPersona(self,Persona):
        self.Persona = Persona

    def SetHora(self,Hora):
        self.Hora = Hora

    def SetBool(self,Bool):
        self.Bool = Bool